            <h3 class="text-head">Atlantic Immigration Pilot Program: How it works</h3>
            The Atlantic Immigration Pilot is an employer-driven program that facilitates the hiring of foreign workers. All principal applicants arriving in Canada under the pilot program must have a job offer from a designated employer and an individualized settlement plan for themselves and their family.
<br><br>Once a designated employer finds a candidate who meets their employment needs and the program criteria, that employer will need to first offer them a job. Employers do not need to go through the process of obtaining a Labour Market Impact Assessment (LMIA) under this program.
