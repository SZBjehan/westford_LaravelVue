                <li>
                    <h3 class="text-head"> Entrepreneur Start-Up Visas </h3>
                    <p> With funding and support from a designated Canadian investment organization, individuals with a plan for a unique business may be able to acquire this visa.</p>
                </li>
                <li>
                    <h3 class="text-head"> Self-Employed Immigration </h3>
                    <p> These programs allow self-employed individuals with significant experience in certain trades and professions to make an application for Canadian immigration.</p>
                </li>
            </ul>
